"""
🧪 Integration tests for repair_atlas views — feedback + verdict polling.

These exercise the two read/write JSON endpoints the tech UI uses *after*
the answer is generated: the 👍/👎 button and the background verifier's
polling endpoint. Both depend on `RepairAnswer` + `RepairSession` so they
need the tenant schema; we reuse the project's `ERPTenantTestCase`.

We do NOT hit the LLM — answers are created in-DB to isolate the view
contract from the generator pipeline (already covered by `test_coach.py`).
"""
from __future__ import annotations

import json

from django.contrib.auth import get_user_model
from django.test import Client as TestClient, override_settings

from inventory.tests.base import ERPTenantTestCase
from repair_atlas.models import (
    RepairSession, RepairQuery, RepairAnswer,
    RepairMode, AnswerSource, ConfidenceTier, VerificationStatus,
)

User = get_user_model()


class _TenantClient(TestClient):
    """Test client that always sends requests to the tenant subdomain so
    django-tenants routes them through TENANT_APPS' URL conf. The
    surrounding test class must `override_settings(ALLOWED_HOSTS=['*'])`
    because the tenant host generated by ERPTenantTestCase
    (`test-xxx.test.com`) isn't in the prod allow-list."""
    def __init__(self, tenant_host: str, **kwargs):
        super().__init__(HTTP_HOST=tenant_host, **kwargs)


@override_settings(ALLOWED_HOSTS=['*'])
class RepairAtlasFeedbackTests(ERPTenantTestCase):
    """👍/👎 endpoint — used by the tech to rate any answer the bot gave."""

    def setUp(self):
        super().setUp()
        self.user = User.objects.create_user(
            username='tech_one', password='pw12345!', email='t@x.com'
        )
        self.other_user = User.objects.create_user(
            username='tech_two', password='pw12345!', email='o@x.com'
        )
        self.session = RepairSession.objects.create(
            user=self.user, brand='BMW', model_name='330i',
        )
        self.query = RepairQuery.objects.create(
            session=self.session, mode=RepairMode.DISASSEMBLY,
            question_text='إزاي أفك الدينمو؟',
        )
        self.answer = RepairAnswer.objects.create(
            query=self.query, body_markdown='1. افصل البطارية...',
            source=AnswerSource.LLM,
            confidence_score=72, confidence_tier=ConfidenceTier.MEDIUM,
        )
        self.client = _TenantClient(tenant_host=self.domain.domain)

    def _login(self, user=None):
        self.client.force_login(user or self.user)

    def test_feedback_thumbs_up_persists(self):
        self._login()
        resp = self.client.post(
            f'/repair-atlas/api/feedback/{self.answer.id}/',
            data=json.dumps({'value': 1}),
            content_type='application/json',
        )
        self.assertEqual(resp.status_code, 200)
        self.assertEqual(resp.json(), {'ok': True, 'value': 1})

        self.answer.refresh_from_db()
        self.assertEqual(self.answer.tech_feedback, 1)
        self.assertIsNotNone(self.answer.tech_feedback_at)

    def test_feedback_thumbs_down_persists(self):
        self._login()
        resp = self.client.post(
            f'/repair-atlas/api/feedback/{self.answer.id}/',
            data=json.dumps({'value': -1}),
            content_type='application/json',
        )
        self.assertEqual(resp.status_code, 200)
        self.answer.refresh_from_db()
        self.assertEqual(self.answer.tech_feedback, -1)

    def test_feedback_clears_to_zero(self):
        self.answer.tech_feedback = 1
        self.answer.save(update_fields=['tech_feedback'])
        self._login()
        resp = self.client.post(
            f'/repair-atlas/api/feedback/{self.answer.id}/',
            data=json.dumps({'value': 0}),
            content_type='application/json',
        )
        self.assertEqual(resp.status_code, 200)
        self.answer.refresh_from_db()
        self.assertEqual(self.answer.tech_feedback, 0)

    def test_feedback_rejects_out_of_range_value(self):
        self._login()
        resp = self.client.post(
            f'/repair-atlas/api/feedback/{self.answer.id}/',
            data=json.dumps({'value': 5}),
            content_type='application/json',
        )
        self.assertEqual(resp.status_code, 400)
        self.assertFalse(resp.json()['ok'])

    def test_feedback_rejects_invalid_json(self):
        self._login()
        resp = self.client.post(
            f'/repair-atlas/api/feedback/{self.answer.id}/',
            data=b'not json at all', content_type='application/json',
        )
        self.assertEqual(resp.status_code, 400)

    def test_feedback_blocks_other_users_answer(self):
        """Tech B must not be able to rate Tech A's answer."""
        self._login(self.other_user)
        resp = self.client.post(
            f'/repair-atlas/api/feedback/{self.answer.id}/',
            data=json.dumps({'value': 1}),
            content_type='application/json',
        )
        self.assertEqual(resp.status_code, 404)
        self.answer.refresh_from_db()
        self.assertEqual(self.answer.tech_feedback, 0)

    def test_feedback_requires_login(self):
        resp = self.client.post(
            f'/repair-atlas/api/feedback/{self.answer.id}/',
            data=json.dumps({'value': 1}),
            content_type='application/json',
        )
        # login_required redirects unauthenticated to /accounts/login/
        self.assertIn(resp.status_code, (302, 403))


@override_settings(ALLOWED_HOSTS=['*'])
class RepairAtlasVerdictPollingTests(ERPTenantTestCase):
    """Polling endpoint — the tech's browser polls this after a fast-path
    answer is shown, waiting for the background Verifier to score it."""

    def setUp(self):
        super().setUp()
        self.user = User.objects.create_user(
            username='tech_three', password='pw12345!', email='t3@x.com'
        )
        self.other_user = User.objects.create_user(
            username='tech_four', password='pw12345!', email='t4@x.com'
        )
        self.session = RepairSession.objects.create(
            user=self.user, brand='Toyota',
        )
        self.query = RepairQuery.objects.create(
            session=self.session, mode=RepairMode.WIRING,
            question_text='ضفيرة طلمبة البنزين',
        )
        self.answer = RepairAnswer.objects.create(
            query=self.query, body_markdown='draft', source=AnswerSource.LLM,
        )
        self.client = _TenantClient(tenant_host=self.domain.domain)
        self.client.force_login(self.user)

    def test_pending_verdict_reports_not_ready(self):
        """Before the Verifier finishes, tier=unknown → ready=False."""
        resp = self.client.get(f'/repair-atlas/api/verdict/{self.answer.id}/')
        self.assertEqual(resp.status_code, 200)
        body = resp.json()
        self.assertFalse(body['ready'])
        self.assertEqual(body['tier'], 'pending')
        self.assertIsNone(body['confidence'])

    def test_high_tier_verdict_reports_ready(self):
        self.answer.confidence_score = 92
        self.answer.confidence_tier = ConfidenceTier.HIGH
        self.answer.auto_promoted = True
        self.answer.verifier_doubts = ['none']
        self.answer.save()
        resp = self.client.get(f'/repair-atlas/api/verdict/{self.answer.id}/')
        body = resp.json()
        self.assertTrue(body['ready'])
        self.assertEqual(body['tier'], ConfidenceTier.HIGH)
        self.assertEqual(body['confidence'], 92)
        self.assertTrue(body['auto_promoted'])

    def test_low_tier_includes_doubts(self):
        self.answer.confidence_score = 30
        self.answer.confidence_tier = ConfidenceTier.LOW
        self.answer.verifier_doubts = ['الكونيكتور غلط', 'pinout مش مؤكد']
        self.answer.save()
        resp = self.client.get(f'/repair-atlas/api/verdict/{self.answer.id}/')
        body = resp.json()
        self.assertTrue(body['ready'])
        self.assertEqual(body['tier'], ConfidenceTier.LOW)
        self.assertEqual(len(body['doubts']), 2)

    def test_verdict_blocks_other_users_answer(self):
        """Tech B polling Tech A's answer must get 404."""
        self.client.force_login(self.other_user)
        resp = self.client.get(f'/repair-atlas/api/verdict/{self.answer.id}/')
        self.assertEqual(resp.status_code, 404)


@override_settings(ALLOWED_HOSTS=['*'])
class RepairAtlasResetTests(ERPTenantTestCase):
    """`/api/reset/` clears the in-session conversation context."""

    def setUp(self):
        super().setUp()
        self.user = User.objects.create_user(
            username='tech_five', password='pw12345!', email='t5@x.com'
        )
        self.client = _TenantClient(tenant_host=self.domain.domain)
        self.client.force_login(self.user)

    def test_reset_clears_session_keys(self):
        sess = self.client.session
        sess['repair_atlas_session_id_v1'] = 999
        sess['repair_atlas_history_v1'] = [{'role': 'user', 'text': 'x'}]
        sess.save()

        resp = self.client.post('/repair-atlas/api/reset/')
        self.assertEqual(resp.status_code, 200)
        self.assertTrue(resp.json()['success'])

        sess = self.client.session
        self.assertNotIn('repair_atlas_session_id_v1', sess)
        self.assertNotIn('repair_atlas_history_v1', sess)

    def test_reset_closes_ai_rooms_conversation(self):
        """Resetting the chat should mark the matching ai_rooms conversation
        as closed (closed_at set) so the hub doesn't show it as ongoing."""
        from ai_rooms.models import AIRoomConversation, RoomKind, Audience
        conv = AIRoomConversation.objects.create(
            user=self.user, room=RoomKind.REPAIR_ATLAS,
            audience=Audience.SHOP, title='draft',
        )
        sess = self.client.session
        sess['ai_rooms_conv_id__repair_atlas__shop'] = conv.id
        sess.save()

        resp = self.client.post('/repair-atlas/api/reset/')
        self.assertEqual(resp.status_code, 200)

        conv.refresh_from_db()
        self.assertIsNotNone(conv.closed_at)
